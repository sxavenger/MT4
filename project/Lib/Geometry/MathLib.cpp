#include "MathLib.h"

////////////////////////////////////////////////////////////////////////////////////////////
// MathLib methods
////////////////////////////////////////////////////////////////////////////////////////////

bool IsNan(const Vector3f& v) {
	return Any(Flag3{std::isnan(v.x), std::isnan(v.y), std::isnan(v.z)});
}

bool IsNan(const Quaternion& q) {
	return Any(Flag4{ std::isnan(q.x), std::isnan(q.y), std::isnan(q.z), std::isnan(q.w)});
}

